package com.googlecode.meteorframework.core;

public class MeteorParseException extends MeteorException {
	private static final long serialVersionUID = 1L;

	public MeteorParseException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public MeteorParseException(String arg0) {
		super(arg0);
	}

	public MeteorParseException(Throwable arg0) {
		super(arg0);
	}

}
